let users = []
console.log(users)

// Adiciona um item no final do Array.
users.push("Rodrigo")
users.push("João")
users.push("Marcos")

// Adiciona um item no inicio do Array.
users.unshift("Ana")

// Remove do início do Array.
users.shift()

// Remove do final do Array.
users.pop()

console.log(users)
